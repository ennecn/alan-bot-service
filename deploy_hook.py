L("# Source 1: task-output.txt in CWD")
L('if [ -n "$CWD" ] && [ -f "$CWD/task-output.txt" ] && [ -s "$CWD/task-output.txt" ]; then')
L('    OUTPUT=$(tail -c 4000 "$CWD/task-output.txt")')
L('    log "Output from $CWD/task-output.txt (${#OUTPUT} chars)"')
L("fi")
L("")
L("# Source 2: result dir fallback")
L('if [ -z "$OUTPUT" ] && [ -f "${RESULT_DIR}/task-output.txt" ] && [ -s "${RESULT_DIR}/task-output.txt" ]; then')
L('    OUTPUT=$(tail -c 4000 "${RESULT_DIR}/task-output.txt")')
L('    log "Output from result dir (${#OUTPUT} chars)"')
L("fi")
L("")
L("# Source 3: directory listing")
L('if [ -z "$OUTPUT" ] && [ -n "$CWD" ] && [ -d "$CWD" ]; then')
L("    FILES=$(ls -1t \"$CWD\" 2>/dev/null | head -20 | tr '\\n' ', ')")
L('    OUTPUT="Working dir: ${CWD}  Files: ${FILES}"')
L('    log "Output from dir listing"')
