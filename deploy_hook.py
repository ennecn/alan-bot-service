L("#!/bin/bash")
L("# Claude Code Stop Hook v3: notify AGI + send Telegram message")
L("# Key change: include results directly in chat.send message")
L("")
L("set -uo pipefail")
L("")
L('RESULT_DIR="/Users/fangjin/claude-code-results"')
